class colors:

    """ A list of ANSI coded colors for use in the terminal.
            Ex: print bcolors.WARNING + "IMMINENT FAILURE IS IMMINENT" +
            bcolors.ENDC.
    """

    HEADER = '\033[95m'
    OKBLUE = '\033[94m'
    OKGREEN = '\033[92m'
    WARNING = '\033[93m'
    FAIL = '\033[91m'
    ENDC = '\033[0m'
    BOLD = '\033[1m'
    UNDERLINE = '\033[4m'
